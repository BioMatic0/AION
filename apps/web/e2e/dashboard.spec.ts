import { expect, test } from "@playwright/test";

test("dashboard renders live counts from intercepted API responses", async ({ page }) => {
  await page.route("http://localhost:4000/**", async (route) => {
    const url = new URL(route.request().url());

    switch (url.pathname) {
      case "/auth/me":
        await route.fulfill({
          status: 200,
          contentType: "application/json",
          body: JSON.stringify({
            user: {
              id: "user-1",
              displayName: "Patrick Wirth",
              email: "patrick@example.com",
              createdAt: "2026-03-08T08:00:00.000Z"
            },
            session: {
              id: "session-1",
              userId: "user-1",
              label: "Browser",
              createdAt: "2026-03-08T08:00:00.000Z",
              lastSeenAt: "2026-03-08T08:00:00.000Z",
              revokedAt: null
            }
          })
        });
        return;
      case "/journal":
        await route.fulfill({
          status: 200,
          contentType: "application/json",
          body: JSON.stringify([
            {
              id: "journal-1",
              title: "Entry 1",
              content: "Reflection",
              entryType: "journal",
              createdAt: "2026-03-08T08:00:00.000Z",
              updatedAt: "2026-03-08T08:00:00.000Z"
            },
            {
              id: "journal-2",
              title: "Entry 2",
              content: "Reflection",
              entryType: "journal",
              createdAt: "2026-03-08T09:00:00.000Z",
              updatedAt: "2026-03-08T09:00:00.000Z"
            }
          ])
        });
        return;
      case "/diary":
        await route.fulfill({
          status: 200,
          contentType: "application/json",
          body: JSON.stringify([
            {
              id: "diary-1",
              title: "Morning",
              content: "Check-in",
              mood: "steady",
              entryDate: "2026-03-08",
              autoGenerated: false,
              createdAt: "2026-03-08T07:00:00.000Z",
              updatedAt: "2026-03-08T07:00:00.000Z"
            }
          ])
        });
        return;
      case "/notes":
        await route.fulfill({
          status: 200,
          contentType: "application/json",
          body: JSON.stringify([
            {
              id: "note-1",
              title: "Idea",
              content: "Hook up memory",
              category: "Ideas",
              tags: ["memory"],
              isPinned: true,
              updatedAt: "2026-03-08T06:00:00.000Z"
            }
          ])
        });
        return;
      case "/goals":
        await route.fulfill({
          status: 200,
          contentType: "application/json",
          body: JSON.stringify({
            items: [],
            stats: {
              total: 1,
              active: 1,
              completed: 0,
              averageProgress: 64,
              completionRate: 0
            }
          })
        });
        return;
      default:
        await route.fulfill({
          status: 404,
          contentType: "application/json",
          body: JSON.stringify({ message: `Unhandled route: ${url.pathname}` })
        });
    }
  });

  await page.goto("/dashboard");

  await expect(page.getByText("Dashboard zeigt Live-Zahlen aus der API.")).toBeVisible();
  await expect(page.getByTestId("dashboard-card-journal")).toContainText("2");
  await expect(page.getByTestId("dashboard-card-diary")).toContainText("1");
  await expect(page.getByTestId("dashboard-card-notes")).toContainText("1");
  await expect(page.getByTestId("dashboard-card-goals")).toContainText("64%");
});
