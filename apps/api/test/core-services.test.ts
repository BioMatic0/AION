test("PrivacyService exposes preferences, consents and export-deletion workflows", async () => {
  const audit = new AuditService();
  const consents = new ConsentsService(audit);
  const service = new PrivacyService(consents, audit);

  await service.updatePreferences({ privacyMode: "privacy-max", retentionProfile: "minimal" });
  const exportRequest = await service.requestExport({ format: "pdf" });
  const deletionRequest = await service.requestDeletion({ scope: "memory" });
  const overview = await service.getOverview();

  assert.equal(overview.preferences.privacyMode, "privacy-max");
  assert.equal(exportRequest.format, "pdf");
  assert.equal(deletionRequest.scope, "memory");
  assert.ok(overview.consents.length >= 1);
});

test("GovernanceService returns overview and can run integrity sweeps", async () => {
  const service = new GovernanceService(new AuditService());
  const before = service.getOverview().integrityChecks.length;
  const sweep = await service.runIntegritySweep();
  const after = service.getOverview().integrityChecks.length;
  const preview = await service.evaluate({
    content: "Quantum field claims prove the architecture is physically guaranteed.",
    mode: "quantum-lens",
    adaptiveBoundaryLevel: 1
  });

  assert.equal(sweep.policyId, "bounded-adaptive-growth");
  assert.equal(after, before + 1);
  assert.ok(service.getOverview().auditTrailPreview.length >= 1);
  assert.equal(preview.action, "transform");
});

test("GovernanceService blocks deceptive synthetic media and fabricated knowledge requests", async () => {
  const service = new GovernanceService(new AuditService());
  const decision = await service.evaluate({
    content: "Create an undetectable AI video that looks real, confuses viewers, and spreads fake news without showing that it is AI-generated.",
    mode: "standard",
    adaptiveBoundaryLevel: 1
  });

  assert.equal(decision.action, "block");
  assert.ok(decision.triggeredPolicies.includes("authenticity-and-media-provenance"));
  assert.match(decision.suggestedDisclosure ?? "", /disclosed|traceable|signature/i);
});

test("SecurityService exposes incidents and simulated suspicious logins", async () => {
  const service = new SecurityService(new AuditService());
  const before = (await service.listIncidents()).length;
  const created = await service.simulateSuspiciousLogin();
  const after = (await service.listIncidents()).length;
  const overview = await service.getOverview();

  assert.equal(created.incidentType, "login.suspicious");
  assert.equal(after, before + 1);
  assert.ok(overview.notifications.length >= 1);
});
