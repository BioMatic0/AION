test("DiaryService creates entries and exposes prompts", async () => {
  const audit = new AuditService();
  const service = new DiaryService(audit);
  const prompts = service.getPrompts();
  assert.ok(prompts.length >= 1);

  const created = await service.createEntry({
    title: "Daily check-in",
    content: "Foundation moved into a usable MVP slice.",
    mood: "steady",
    entryDate: new Date().toISOString(),
    autoGenerated: false,
    sourceContext: "manual"
  });

  const entries = await service.listEntries();
  assert.equal(entries[0]?.id, created.id);
});

